#!/bin/bash
#
# 工具类

#######################################
# 输出标准日志
# Globals:
# Arguments:
#   $1:日志信息
# Returns:
#   None
#######################################
util::log_info() {
    echo "[$(date +'%Y-%m-%d %H:%M:%S:%s')]-[INFO]: $@" >&1
}

#######################################
# 输出错误日志
# Globals:
# Arguments:
#   $1:日志信息
# Returns:
#   None
#######################################
util::log_err() {
    echo "[$(date +'%Y-%m-%d %H:%M:%S:%s')]-[ERROR]: $@" >&2
}